/**
 * - setSeconds() - устанавливает секунды указанной даты по местному времени.
 * - Параметры - .setSeconds(seconds, ms?)
 *
 *   - seconds - Целое число от 0 до 59, представляющее секунды.
 *   - ms - Необязательный параметр. Целое число от 0 до 999, представляющее миллисекунды.
 *
 * Пример:
 */

const theBigDay = new Date();
theBigDay.setSeconds(30);
