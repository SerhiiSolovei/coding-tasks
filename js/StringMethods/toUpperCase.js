/**
 * Преобразование в верхний и нижний регистры
 * #1
 * .toUpperCase() - возвращает значение строки, на которой он был вызван, преобразованное в верхний регистр.
 *
 * Параметры: -
 *
 * Примеры:
 */

"алфавит".toUpperCase(); // 'АЛФАВИТ'

const text = "alpHAbet";

console.log(text.toUpperCase()); // "ALPHABET"
